dividendo = int(input("Digite o valor do dividendo: "))
divisor = int(input("Digite o valor do divisor: "))

quociente = dividendo // divisor
resto = dividendo % divisor

print(f"O valor do dividendo é: {dividendo}")
print(f"O valor do divisor é: {divisor}")
print(f"O valor do quociente é: {quociente}")
print(f"O valor do resto é: {resto}")
